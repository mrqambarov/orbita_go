/* ---- i18n Engine ---- */
(function () {
    const STORAGE_KEY = 'orbita_lang';

    function t(key) {
        const lang = localStorage.getItem(STORAGE_KEY) || 'uz';
        return (window.TRANSLATIONS[lang] && window.TRANSLATIONS[lang][key]) ||
               (window.TRANSLATIONS['uz'][key]) || key;
    }

    function applyTranslations() {
        document.querySelectorAll('[data-i18n]').forEach(el => {
            const key = el.getAttribute('data-i18n');
            const attr = el.getAttribute('data-i18n-attr');
            if (attr) {
                el.setAttribute(attr, t(key));
            } else {
                el.innerHTML = t(key);
            }
        });
        // Update active button
        const lang = localStorage.getItem(STORAGE_KEY) || 'uz';
        document.querySelectorAll('.lang-btn').forEach(btn => {
            btn.classList.toggle('active', btn.dataset.lang === lang);
        });
        document.documentElement.lang = lang;
    }

    function switchLang(lang) {
        localStorage.setItem(STORAGE_KEY, lang);
        applyTranslations();
    }

    // Public API
    window.i18n = { t, apply: applyTranslations, switch: switchLang };

    // Auto apply on load
    document.addEventListener('DOMContentLoaded', () => {
        applyTranslations();
        document.querySelectorAll('.lang-btn').forEach(btn => {
            btn.addEventListener('click', () => switchLang(btn.dataset.lang));
        });
    });
})();
